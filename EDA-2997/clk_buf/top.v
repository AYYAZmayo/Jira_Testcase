/*
  Primitive Description:

          |-------------------------------------------------------------------------------------------|
          |                                                                                           |
          |   |-------|                                 |----------------------------|                |
    din --|-->| I_BUF | ------------------------------> |                            |                |
          |   |-------|                                 |                            |                |
          |                                             |                            |                |
          |                                             |           Fabric           |                |
          |                                             |                            |                |
          |                                             |                            |                |
          |   |-------|     |---------|     |-----|     |                            |                |
    clk --|-->| I_BUF | --> | CLK_BUF | --> | PLL | --> |                            |                |
          |   |-------|     |---------|     |-----|     |                            |                |
          |                                             |                            |                |
          |                                             |                            |                |
          |                                             |                            |     |-------|  |
          |                                             |                            | --> | O_BUF |--|--> dout
          |                                             |----------------------------|     |-------|  |
          |                                                                                           |
          |-------------------------------------------------------------------------------------------|
*/

module top (
  input wire din,
  input wire clk,
  output reg dout
);
  PLL #(
    .PLL_MULT(16),
    .PLL_DIV(1),
    .PLL_POST_DIV(34)
  ) pll (
    .PLL_EN(1'b1),
    .CLK_IN(clk),
    .CLK_OUT(pll_clk),
    .CLK_OUT_DIV2(),
    .CLK_OUT_DIV3(),
    .CLK_OUT_DIV4()
  );
  
  always @ (posedge pll_clk) begin
    dout <= din;
  end
endmodule
